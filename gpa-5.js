//write a program that give the grade of the student based on the marks



var result = 90;

if (result >= 90 && result <= 100) {
  console.log("You have got Golden A+");
} else if (result >= 80 && result <= 89) {
  console.log("You have got A+");
} else if (result >= 70 && result <= 79) {
  console.log("You have got A-");
} else if (result >= 60 && result <= 69) {
  console.log("You have got B");
} else if (result >= 50 && result <= 59) {
  console.log("You have got C");
} else if (result >= 40 && result <= 49) {
  console.log("You have got D");
} else if (result >= 30 && result <= 39) {
  console.log("You have got E");
} else {
  console.log("You ar failed");
}
